char *checksum(unsigned char *frame, unsigned char lower, unsigned char upper, unsigned char flag)
{
	unsigned int sum = 0, pseudo = 0;	
	unsigned char i;
	if ((upper - lower + 1) % 2)
	{
		for (i = lower; i < upper - 1; i += 2)
			sum += ((frame[i] << 8) + frame[i + 1]);
		sum += frame[upper];
	}
	else
		for (i = lower; i < upper; i += 2)
			sum += ((frame[i] << 8) + frame[i + 1]);
	
	if (flag)
	{
		for (i = 26; i < 33; i += 2)
			pseudo += ((frame[i] << 8) + frame[i + 1]);
		if ((frame[23] ^ 17))
		{
			if ((frame[23] ^ 6))
			{
				if ((frame[23] ^ 1)){}
				else
					sum = sum;
			}
			else
				sum += (pseudo + ((frame[lower + 12] >> 4) << 2) + frame[23]);
		}
		else
			sum += (pseudo + ((frame[lower + 4] << 8) + frame[lower + 5]) + frame[23]);
	}
	//printf("%x\n", (((unsigned short)sum + (sum >> 16)))^ 0xffff);
	return (((unsigned short)sum + (sum >> 16)) ^ 0xffff) ? "Trama Invalida" : "Trama Valida\t";
}

void printMAC(unsigned char *frame, unsigned char i)
{
	printf("%.2X:%.2X:%.2X:%.2X:%.2X:%.2X\t\t|", frame[i], frame[i + 1], frame[i + 2], frame[i + 3], frame[i + 4], frame[i + 5]);
}

void printIP(unsigned char *frame, unsigned char i)
{
	printf("%.3d.%.3d.%.3d.%.3d\t\t|\n", frame[i], frame[i + 1], frame[i + 2], frame[i + 3]);
}

